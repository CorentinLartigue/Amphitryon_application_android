package com.example.lamphitryon;

public class Categorie {
    private Integer numero;
    private String nom;


    public Categorie(Integer numero, String nom) {
        this.numero = numero;
        this.nom = nom;
    }

    public Integer getNumero() {
        return numero;
    }

    public String getNom() {
        return nom;
    }


}

